package tuleles;

/**
 * A megeheto dolgok implementalasa
 */
public class Kaja {
    private int burgonya;
    private int hal;


    public Kaja(int burgonya, int hal) {
        this.burgonya = burgonya;
        this.hal = hal;

    }

    public int getBurgonya() {
        return burgonya;
    }

    public void setBurgonya(int burgonya) {
        this.burgonya = burgonya;
    }

    public int getHal() {
        return hal;
    }

    public void setHal(int hal) {
        this.hal = hal;
    }




}
